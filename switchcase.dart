// learn about the switch case in dart

void main() {
  // lets do the day of the week
  int dayOfWeek = 3;
  switch (dayOfWeek) {
    case 1:
      {
        print("Sunday");
        break;
      }
    case 2:
      {
        print("Monday");
        break;
      }
    case 3:
      {
        print("Tuesday");
        break;
      }
    case 4:
      {
        print("Wednesday");
        break;
      }
    case 5:
      {
        print("Thursday");
        break;
      }
    case 6:
      {
        print("Friday");
        break;
      }
    case 7:
      {
        print("Saturday");
        break;
      }
  }

  // the difference with any other language is that curly braces after every case
}
